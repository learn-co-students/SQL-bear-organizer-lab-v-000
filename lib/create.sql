CREATE TABLE bears (
  id INTEGER PRIMARY KEY,
  name TEXT,
  age INTEGER,
  gender CHAR,  --"M" or "F"
  color TEXT,
  temperament TEXT,
  alive BOOLEAN    --"0" or "1" in sqlite3
);


